using System;

namespace OpAtribuicao
{
    class Program
    {
        static void Main(string[] args)
        {
            //Operadores de Atribuição
            int a = 10;
            Console.WriteLine(a);
            a += 2;
            Console.WriteLine(a);
            a *= 3;
            Console.WriteLine(a);
            string s = "ABC";
            Console.WriteLine(s);
            s += "DEF";
            Console.WriteLine(s);

            //Operadores Aritmeticos / Atribuição
            int b = 10;
            b++;
            Console.WriteLine(b);

            int c = 10;
            c--;
            Console.WriteLine(c);


        }
    }
}
